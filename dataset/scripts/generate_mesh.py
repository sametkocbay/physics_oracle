"""§4 — deterministic Gmsh mesh generator for the NACA airfoil cases.

Identical topology and field setup for every case (§4: "same airfoil
coordinates → same mesh, always"); only the airfoil point list and the
boundary-layer first-cell height vary.  First-layer height is scaled with Re
so wall y+ stays below 1 (§4.2).
"""
from __future__ import annotations

import argparse
import math
import re
import shutil
import subprocess
from pathlib import Path

import numpy as np

from common import (
    CHORD,
    NU,
    naca4_coordinates,
    parse_case_id,
    setup_logging,
)

LOG = setup_logging()


# ---------------------------------------------------------------------------
# Mesh sizing
# ---------------------------------------------------------------------------

DOMAIN_CHORDS_UPSTREAM = 20.0          # §4.2 far-field ≥ 20c
DOMAIN_CHORDS_DOWNSTREAM = 25.0
DOMAIN_CHORDS_VERTICAL = 20.0

LC_FAR = 0.8                           # far-field cell size (coarser → fewer cells)
LC_WAKE = 0.05                         # wake refinement
LC_AIRFOIL_BASE = 0.006                # tangential along airfoil

# §4.2 wants 30+ cells in the boundary layer with growth < 1.2.
BL_LAYERS = 30
BL_GROWTH = 1.18

# Threshold gradient (size-field transition).  Stretched out enough to avoid
# the numerical-diffusion stripe at the refinement boundary, but not so wide
# that the cell count blows up.  Sigmoid + StopAtDistMax give a smooth S
# curve from lcNear at DistMin to lcFar at DistMax, then constant beyond.
THRESHOLD_DIST_MIN = 0.3                # near-airfoil region (chord)
THRESHOLD_DIST_MAX = 8.0
BL_FAR_SIZE = LC_AIRFOIL_BASE           # cell size at the BL outer edge


def first_layer_for_yplus(target_yplus: float, re_value: float, chord: float = CHORD) -> float:
    """Approximate first-cell height for a given y+ on a turbulent flat plate.

    C_f ≈ 0.026 · Re^(-1/7);  u_τ = U·√(C_f/2);  y(y+) = y+·ν / u_τ.
    """
    cf = 0.026 / re_value ** (1.0 / 7.0)
    u_mag = re_value * NU / chord
    u_tau = u_mag * math.sqrt(0.5 * cf)
    return target_yplus * NU / u_tau


def airfoil_bounds(coords: np.ndarray, margin: float = 1e-3) -> dict:
    """Return a bounding box slightly larger than the airfoil polyline so
    `Curve In BoundingBox` captures all surface curves."""
    return {
        "x_min": float(coords[:, 0].min()) - margin,
        "x_max": float(coords[:, 0].max()) + margin,
        "y_min": float(coords[:, 1].min()) - margin,
        "y_max": float(coords[:, 1].max()) + margin,
    }


# ---------------------------------------------------------------------------
# .geo writer
# ---------------------------------------------------------------------------

GEO_HEADER = """// Auto-generated by dataset/scripts/generate_mesh.py — do not edit.
// case_id={case_id}  naca={naca_code}  Re={re_value:.3e}
SetFactory("OpenCASCADE");

chord = {chord};
xMin  = {x_min};
xMax  = {x_max};
yMin  = {y_min};
yMax  = {y_max};
dz    = 0.01 * chord;
eps   = 1e-6;

lcFar  = {lc_far};
lcNear = {lc_near};
lcWake = {lc_wake};

nBLayers   = {bl_layers};
blGrowth   = {bl_growth};
firstLayer = {first_layer:.6e};
blThickness = firstLayer * (blGrowth^nBLayers - 1.0) / (blGrowth - 1.0);
blFarSize   = {bl_far_size};

// Mesh topology, in radial order from the wall:
//   - BL field with `Quads = 1` + the `BoundaryLayer Field = 1;` directive
//     below extrudes flat quad layers (= flat hex cells once z-extruded).
//   - Outside the BL we use Frontal-Delaunay triangles (= triangular prisms
//     after extrusion).  This is the "transition" region in the user's
//     mental model.
//   - Algorithm 8 (Frontal-Delaunay for Quads) and `RecombineAll = 1` were
//     both tried for an all-quad outer mesh but the former segfaulted on
//     this OpenCASCADE+BL geometry and the latter blew the gmsh budget on
//     the 20c domain.  Triangular prism outer mesh is the robust choice.
Mesh.Algorithm                  = 6;
Mesh.MeshSizeFromPoints         = 0;
Mesh.MeshSizeFromCurvature      = 0;
Mesh.MeshSizeExtendFromBoundary = 0;
Mesh.Optimize                   = 1;
Mesh.OptimizeNetgen             = 1;
Mesh.Smoothing                  = 20;
Mesh.MshFileVersion             = 2.2;

Rectangle(1) = {{xMin, yMin, 0.0, xMax - xMin, yMax - yMin}};
"""


def write_geo(geo_path: Path, case_id: str, coords: np.ndarray, re_value: float) -> None:
    """Emit the parameterized Gmsh .geo file."""
    naca_code = parse_case_id(case_id)["naca_code"]
    bounds = airfoil_bounds(coords)
    first_layer = first_layer_for_yplus(target_yplus=0.3, re_value=re_value)

    lines = [GEO_HEADER.format(
        case_id=case_id,
        naca_code=naca_code,
        re_value=re_value,
        chord=CHORD,
        x_min=-DOMAIN_CHORDS_UPSTREAM * CHORD,
        x_max=DOMAIN_CHORDS_DOWNSTREAM * CHORD,
        y_min=-DOMAIN_CHORDS_VERTICAL * CHORD,
        y_max=DOMAIN_CHORDS_VERTICAL * CHORD,
        lc_far=LC_FAR,
        lc_near=LC_AIRFOIL_BASE,
        lc_wake=LC_WAKE,
        bl_layers=BL_LAYERS,
        bl_growth=BL_GROWTH,
        first_layer=first_layer,
        bl_far_size=BL_FAR_SIZE,
    )]

    # ---- airfoil points ----
    point_id_start = 100
    point_ids = list(range(point_id_start, point_id_start + len(coords)))
    lines.append("\n// Airfoil surface points (TE -> upper -> LE -> lower -> TE, cosine-spaced)")
    for pid, (x, y) in zip(point_ids, coords):
        lines.append(f"Point({pid}) = {{{x:.10f}, {y:.10f}, 0.0, lcNear}};")

    # ---- airfoil spline: single continuous BSpline, close TE with a short line ----
    # With the open-TE NACA formula, upper TE and lower TE are distinct
    # points.  The airfoil polygon is therefore two curves:
    #   201 = full airfoil BSpline (upper TE → LE → lower TE)
    #   202 = TE base line         (lower TE → upper TE)   -- ~0.25%c segment
    # Using a single BSpline means the LE is an interior control point rather
    # than a topological vertex where two curves meet.  A shared endpoint at
    # the stagnation point with no FanPointsList causes the BoundaryLayer field
    # to emit overlapping or void extrusion normals there, producing the
    # spurious leading-edge separation artifact.
    upper_te_id = point_ids[0]
    lower_te_id = point_ids[-1]
    lines.append("")
    lines.append(f"BSpline(201) = {{{', '.join(map(str, point_ids))}}};  // full airfoil: upper TE -> LE -> lower TE")
    lines.append(f"Line(202) = {{{lower_te_id}, {upper_te_id}}};  // TE base (closes the polygon)")
    lines.append("Curve Loop(2) = {201, 202};")
    lines.append("Plane Surface(2) = {2};")
    lines.append("")
    lines.append("fluid2D[] = BooleanDifference{ Surface{1}; Delete; }{ Surface{2}; Delete; };")

    # ---- physical curve identification by bounding boxes ----
    lines.append("")
    lines.append("inletCurves[]   = Curve In BoundingBox {xMin - eps, yMin - eps, -eps, "
                 "xMin + eps, yMax + eps,  eps};")
    lines.append("outletCurves[]  = Curve In BoundingBox {xMax - eps, yMin - eps, -eps, "
                 "xMax + eps, yMax + eps,  eps};")
    lines.append("bottomCurves[]  = Curve In BoundingBox {xMin - eps, yMin - eps, -eps, "
                 "xMax + eps, yMin + eps,  eps};")
    lines.append("topCurves[]     = Curve In BoundingBox {xMin - eps, yMax - eps, -eps, "
                 "xMax + eps, yMax + eps,  eps};")
    lines.append(
        f"airfoilCurves[] = Curve In BoundingBox {{{bounds['x_min']:.6f}, "
        f"{bounds['y_min']:.6f}, -eps, {bounds['x_max']:.6f}, "
        f"{bounds['y_max']:.6f},  eps}};"
    )

    # ---- mesh size fields ----
    wake_ymin = float(coords[:, 1].min()) - 1.5
    wake_ymax = float(coords[:, 1].max()) + 1.5
    lines.append(f"""
// Field[1] is THE boundary-layer field (extrudes structured quad layers
// outward from the airfoil), declared via the `BoundaryLayer Field = 1;`
// directive at the bottom.  It is NOT included in the size-field Min, so
// the cells inside the BL come from extrusion rather than from the size
// field — that's what gives the flat hex elements next to the wall.
//
// The polyline has an open trailing edge (≈ 0.25 %c base, three curves —
// upper BSpline + lower BSpline + TE-base line), so the BL extrusion
// closes naturally without FanPointsList — which on cambered airfoils
// produced unclassified `defaultFaces` after gmshToFoam.
Field[1] = BoundaryLayer;
Field[1].CurvesList            = {{airfoilCurves[]}};
Field[1].Size                  = firstLayer;
Field[1].SizeFar               = blFarSize;
Field[1].Thickness             = blThickness;
Field[1].Ratio                 = blGrowth;
Field[1].NbLayers              = nBLayers;
Field[1].IntersectMetrics      = 1;
Field[1].Quads                 = 1;

Field[2] = Distance;
Field[2].CurvesList = {{airfoilCurves[]}};
Field[2].Sampling   = 250;

// Smooth size transition from lcNear at the airfoil out to lcFar in the
// outer domain.  Sigmoid + StopAtDistMax avoid the abrupt size-field jump
// that produced the curved velocity stripe upstream of the airfoil.
Field[3] = Threshold;
Field[3].InField        = 2;
Field[3].SizeMin        = lcNear;
Field[3].SizeMax        = lcFar;
Field[3].DistMin        = {THRESHOLD_DIST_MIN};
Field[3].DistMax        = {THRESHOLD_DIST_MAX};
Field[3].Sigmoid        = 1;
Field[3].StopAtDistMax  = 1;

Field[4] = Box;
Field[4].VIn      = lcWake;
Field[4].VOut     = lcFar;
Field[4].XMin     = 0.99;
Field[4].XMax     = 12.0;
Field[4].YMin     = {wake_ymin:.4f};
Field[4].YMax     = {wake_ymax:.4f};
Field[4].ZMin     = -eps;
Field[4].ZMax     =  eps;
Field[4].Thickness = 1.5;

Field[5] = Min;
Field[5].FieldsList = {{3, 4}};
Background Field = 5;
BoundaryLayer Field = 1;

extruded[] = Extrude {{0.0, 0.0, dz}}
{{
    Surface{{fluid2D[0]}};
    Layers{{1}};
    Recombine;
}};

inletSurfaces[]   = Surface In BoundingBox {{xMin - eps, yMin - eps, -eps, xMin + eps, yMax + eps, dz + eps}};
outletSurfaces[]  = Surface In BoundingBox {{xMax - eps, yMin - eps, -eps, xMax + eps, yMax + eps, dz + eps}};
bottomSurfaces[]  = Surface In BoundingBox {{xMin - eps, yMin - eps, -eps, xMax + eps, yMin + eps, dz + eps}};
topSurfaces[]     = Surface In BoundingBox {{xMin - eps, yMax - eps, -eps, xMax + eps, yMax + eps, dz + eps}};
airfoilSurfaces[] = Surface In BoundingBox {{{bounds['x_min']:.6f}, {bounds['y_min']:.6f}, -eps, {bounds['x_max']:.6f}, {bounds['y_max']:.6f}, dz + eps}};
frontSurfaces[]   = Surface In BoundingBox {{xMin - eps, yMin - eps, -eps,    xMax + eps, yMax + eps,  eps}};
backSurfaces[]    = Surface In BoundingBox {{xMin - eps, yMin - eps, dz - eps, xMax + eps, yMax + eps, dz + eps}};

Physical Surface("inlet")        = {{inletSurfaces[]}};
Physical Surface("outlet")       = {{outletSurfaces[]}};
Physical Surface("top")          = {{topSurfaces[]}};
Physical Surface("bottom")       = {{bottomSurfaces[]}};
Physical Surface("airfoilWalls") = {{airfoilSurfaces[]}};
Physical Surface("frontAndBack") = {{frontSurfaces[], backSurfaces[]}};
Physical Volume("fluid")         = {{extruded[1]}};

Mesh 3;
""")

    geo_path.parent.mkdir(parents=True, exist_ok=True)
    geo_path.write_text("\n".join(lines))


# ---------------------------------------------------------------------------
# Boundary file patching
# ---------------------------------------------------------------------------

# Patch types set after gmshToFoam.  top/bottom use "patch" so that
# the freestream BC (which requires patch type, not symmetryPlane) can be
# applied; this lets the angled far-field flow pass through freely at any AoA.
PATCH_TYPES = {
    "frontAndBack":  ("empty", "empty"),
    "top":           ("patch", "patch"),
    "bottom":        ("patch", "patch"),
    "airfoilWalls":  ("wall", "wall"),
    "inlet":         ("patch", "patch"),
    "outlet":        ("patch", "patch"),
}


def patch_boundary_file(boundary_path: Path) -> None:
    """Re-type patches in constant/polyMesh/boundary after gmshToFoam.

    If gmshToFoam reports a `defaultFaces` patch this function still raises
    via simpleFoam later (no field has a defaultFaces BC entry).  That is
    intentional — it catches a real meshing bug rather than papering over
    it.  The likely cause is a duplicate vertex on the airfoil polyline at
    LE or TE; check `naca4_coordinates` if you see this.
    """
    text = boundary_path.read_text()
    if "defaultFaces" in text:
        LOG.warning("polyMesh contains defaultFaces — fan extrusion likely "
                    "produced unclassified TE faces.  Subsequent simpleFoam "
                    "will fail; investigate the airfoil polyline.")
    for name, (typ, phys) in PATCH_TYPES.items():
        block_re = re.compile(
            rf"({name}\s*\{{[^}}]*?)type\s+\w+;([^}}]*?)physicalType\s+\w+;",
            re.DOTALL,
        )
        text, n_subs = block_re.subn(
            lambda m, typ=typ, phys=phys: f"{m.group(1)}type            {typ};{m.group(2)}physicalType    {phys};",
            text,
        )
        if n_subs == 0:
            LOG.debug("patch %s: no replacement (already correct?)", name)
    boundary_path.write_text(text)


# ---------------------------------------------------------------------------
# Mesh quality parsing
# ---------------------------------------------------------------------------

def parse_check_mesh(log_text: str) -> dict:
    out = {"max_non_orthogonality": None, "max_skewness": None, "n_cells": None,
           "errors": [], "warnings": []}
    m = re.search(r"Max\s+non-orthogonality\s*=\s*([\d.eE+-]+)", log_text)
    if m: out["max_non_orthogonality"] = float(m.group(1))
    m = re.search(r"Max\s+skewness\s*=\s*([\d.eE+-]+)", log_text)
    if m: out["max_skewness"] = float(m.group(1))
    m = re.search(r"cells:\s+(\d+)", log_text)
    if m: out["n_cells"] = int(m.group(1))
    if "***" in log_text or "FOAM FATAL" in log_text:
        out["errors"] = [ln for ln in log_text.splitlines() if "***" in ln or "FATAL" in ln][:10]
    return out


# ---------------------------------------------------------------------------
# Top-level: build mesh inside an OpenFOAM case directory
# ---------------------------------------------------------------------------

def generate_mesh(of_case_dir: Path, case_id: str) -> dict:
    """Generate the Gmsh mesh for an OpenFOAM case and import it.

    Caller has already created the OF case directory tree.  This function
    writes ``system/naca_airfoil.geo``, runs gmsh / gmshToFoam, and patches
    the boundary file.
    """
    parsed = parse_case_id(case_id)
    coords = naca4_coordinates(parsed["naca_code"], n_points=160)

    geo_path = of_case_dir / "system" / "naca_airfoil.geo"
    msh_path = of_case_dir / "naca_airfoil.msh"
    poly_mesh_dir = of_case_dir / "constant" / "polyMesh"

    write_geo(geo_path, case_id, coords, re_value=parsed["Re"])
    # Note: §4.2 calls for y+ < 1 at the wall.  We size the first cell from a
    # turbulent flat-plate u_τ estimate; converged u_τ is typically ~2× higher
    # at the LE/TE so the realised y+ can drift up to ~5 — flagged by §7 QC.

    # Clean any prior mesh
    if poly_mesh_dir.exists():
        shutil.rmtree(poly_mesh_dir)

    LOG.info("[%s] gmsh -3 %s", case_id, geo_path.name)
    res = subprocess.run(
        ["gmsh", "-3", str(geo_path), "-format", "msh2", "-o", str(msh_path)],
        cwd=of_case_dir, capture_output=True, text=True, timeout=900,
    )
    (of_case_dir / "gmsh.log").write_text(res.stdout + res.stderr)
    if res.returncode != 0:
        raise RuntimeError(f"gmsh failed for {case_id}: see gmsh.log")

    LOG.info("[%s] gmshToFoam %s", case_id, msh_path.name)
    res = subprocess.run(
        ["gmshToFoam", str(msh_path)],
        cwd=of_case_dir, capture_output=True, text=True, timeout=600,
    )
    (of_case_dir / "gmshToFoam.log").write_text(res.stdout + res.stderr)
    if res.returncode != 0:
        raise RuntimeError(f"gmshToFoam failed for {case_id}: see gmshToFoam.log")
    msh_path.unlink(missing_ok=True)

    patch_boundary_file(of_case_dir / "constant" / "polyMesh" / "boundary")

    LOG.info("[%s] checkMesh", case_id)
    res = subprocess.run(
        ["checkMesh"],
        cwd=of_case_dir, capture_output=True, text=True, timeout=600,
    )
    log_text = res.stdout + res.stderr
    (of_case_dir / "checkMesh.log").write_text(log_text)
    quality = parse_check_mesh(log_text)
    if res.returncode != 0:
        LOG.warning("checkMesh nonzero exit for %s: %s", case_id, quality.get("errors"))
    LOG.info("[%s] mesh: %s cells, max non-orth %s, max skew %s",
             case_id, quality["n_cells"], quality["max_non_orthogonality"], quality["max_skewness"])
    return quality


# ---------------------------------------------------------------------------
# CLI
# ---------------------------------------------------------------------------

def parse_args() -> argparse.Namespace:
    p = argparse.ArgumentParser(description="Generate Gmsh mesh for one case.")
    p.add_argument("case_id", help="e.g. NACA2412_p5.0_1.5e6")
    p.add_argument("--of-case", required=True, type=Path, help="Path to of_case/ directory")
    return p.parse_args()


def main() -> None:
    args = parse_args()
    args.of_case.mkdir(parents=True, exist_ok=True)
    (args.of_case / "system").mkdir(exist_ok=True)
    (args.of_case / "constant").mkdir(exist_ok=True)
    generate_mesh(args.of_case, args.case_id)


if __name__ == "__main__":
    main()
